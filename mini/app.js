function getChatContextMessages(text, contextImage) {
  const history = state.chat
    .filter((message) => ["user", "assistant"].includes(message.role) && message.text)
    .slice(-CHAT_MEMORY_ROUNDS * 2);
  const messages = history.map((message) => ({ role: message.role, content: message.text }));
  const currentContent = contextImage
    ? [{ type: "text", text }].concat([{ type: "image_url", image_url: { url: contextImage } }])
    : text;
  messages.push({ role: "user", content: currentContent });
  return messages;
}

function updateChatMemoryStatus() {
  if (!chatMemoryStatus) return;
  const turns = Math.ceil(state.chat.filter((message) => message.role === "user").length);
  chatMemoryStatus.textContent = `记忆已开启 · 最近 ${Math.min(turns, CHAT_MEMORY_ROUNDS)} 轮`;
}

function renderChat() {
  chatMessages.innerHTML = "";

  if (!state.chat.length) {
    state.chat = getDefaultChatMessages();
  }

  state.chat.forEach((message) => {
    const item = document.createElement("div");
    item.className = `chat-message ${message.role}`;
    item.innerHTML = `<strong>${message.role === "user" ? "你" : "ZHUANG-AI mini"}</strong>${message.text}`;
    chatMessages.appendChild(item);
  });

  updateChatMemoryStatus();
  chatMessages.scrollTop = chatMessages.scrollHeight;
}

