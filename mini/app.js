function createSelectionFeatherMask(width, height) {
  const canvas = document.createElement("canvas");
  canvas.width = width;
  canvas.height = height;
  const context = canvas.getContext("2d");
  const imageData = context.createImageData(width, height);
  const featherSize = Math.max(1, Math.min(width, height) * 0.3);

  for (let y = 0; y < height; y += 1) {
    for (let x = 0; x < width; x += 1) {
      const edgeDistance = Math.min(x, y, width - 1 - x, height - 1 - y);
      const alpha = Math.max(0, Math.min(1, edgeDistance / featherSize));
      imageData.data[(y * width + x) * 4 + 3] = Math.round(alpha * 255);
    }
  }

  context.putImageData(imageData, 0, 0);
  return canvas;
}

