function constrainSelection(start, end) {
  const imageWidth = end.width;
  const imageHeight = end.height;
  const dx = end.x - start.x;
  const dy = end.y - start.y;
  let width = Math.abs(dx);
  let height = Math.abs(dy);

  if (lockRatio.checked && width > 0 && height > 0) {
    const ratio = getSelectionRatioValue();

    if (width / height > ratio) {
      width = height * ratio;
    } else {
      height = width / ratio;
    }
  }

  const x = dx < 0 ? start.x - width : start.x;
  const y = dy < 0 ? start.y - height : start.y;
  const clampedX = Math.min(Math.max(x, 0), imageWidth - width);
  const clampedY = Math.min(Math.max(y, 0), imageHeight - height);

  return {
    x: clampedX,
    y: clampedY,
    width,
    height,
    displayWidth: imageWidth,
    displayHeight: imageHeight,
  };
}

