function renderPromptPresetFilter() {
  if (!promptPresetCategory || !promptPresetSelect) return;

  updatePseudoSelectOptions(promptPresetMajor, presetMajorCategories.map((item) => ({ value: item.key, label: item.label })), activePromptPresetMajor);
  const buckets = getPresetBuckets(activePromptPresetMajor);
  if (!buckets.length) return;
  if (!buckets.some((bucket) => bucket.key === activePromptPresetCategory)) activePromptPresetCategory = buckets[0]?.key || "其他";
  updatePseudoSelectOptions(promptPresetCategory, buckets.map((bucket) => ({ value: bucket.key, label: `${bucket.icon} ${bucket.label} · ${bucket.items.length}` })), activePromptPresetCategory);

  const filteredItems = getPromptPresetItems();
  const value = filteredItems[Number(activePromptPresetContent)] ? activePromptPresetContent : "0";
  updatePseudoSelectOptions(promptPresetSelect, getPresetSelectOptions(filteredItems), value);
}

function renderPresets() {
  presetList.innerHTML = "";

  const buckets = getPresetBuckets();
  if (!buckets.some((bucket) => bucket.key === activePresetCategory)) activePresetCategory = buckets[0]?.key || "其他";
  updatePseudoSelectOptions(presetCategorySelect, buckets.map((bucket) => ({ value: bucket.key, label: `${bucket.icon} ${bucket.label} · ${bucket.items.length}` })), activePresetCategory);

  const activeBucket = buckets.find((bucket) => bucket.key === activePresetCategory) || buckets[0];
  if (!activeBucket) return;

  const filteredItems = getPresetLibraryItems();
  const value = filteredItems[Number(activePresetContent)] ? activePresetContent : "0";
  updatePseudoSelectOptions(presetContentSelect, getPresetSelectOptions(filteredItems), value);

  const head = document.createElement("div");
  head.className = "preset-group-head";
  head.innerHTML = `<h4>${activeBucket.icon} ${activeBucket.label}</h4><span>${filteredItems.length}/${activeBucket.items.length} 个预设</span>`;
  presetList.appendChild(head);

  if (!filteredItems.length) {
    const empty = document.createElement("div");
    empty.className = "notice-box";
    empty.textContent = "没有匹配的预设";
    presetList.appendChild(empty);
    return;
  }

  const preset = filteredItems[Number(getPseudoSelectValue(presetContentSelect))] || filteredItems[0];
  const item = document.createElement("div");
  item.className = "preset-item";

  const title = document.createElement("h4");
  title.textContent = preset.title;
  const text = document.createElement("p");
  text.textContent = preset.text;
  const apply = document.createElement("button");
  apply.className = "mini-button primary small";
  apply.type = "button";
  apply.textContent = "应用到创作";
  apply.addEventListener("click", () => {
    applyPreset(preset);
    switchTab("create");
  });
  item.append(title, text, apply);
  presetList.appendChild(item);
}

