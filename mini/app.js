async function requestJson(url, apiKey, body, options = {}) {
  const maxRetries = options.retries ?? 1;
  const timeoutMs = options.timeoutMs ?? 180000;
  const method = options.method || "POST";

  for (let attempt = 0; attempt <= maxRetries; attempt += 1) {
    const controller = new AbortController();
    const timeoutId = window.setTimeout(() => controller.abort(), timeoutMs);

    try {
      const response = await fetch(url, {
        method,
        headers: {
          "Content-Type": "application/json",
          Authorization: `Bearer ${normalizeApiKey(apiKey)}`,
        },
        body: method === "GET" ? undefined : JSON.stringify(body),
        signal: controller.signal,
      });

      const text = await response.text();
      const data = parseResponseText(text);
      if (!response.ok) {
        if (response.status >= 500 && attempt < maxRetries) continue;
        throw new Error(getApiErrorMessage(data, response.status));
      }
      return data;
    } catch (error) {
      if (error.name === "AbortError") {
        if (attempt < maxRetries) continue;
        throw new Error("请求超时，请稍后重试");
      }
      if (error instanceof TypeError && attempt < maxRetries) continue;
      throw error;
    } finally {
      window.clearTimeout(timeoutId);
    }
  }

  throw new Error("请求失败，请稍后重试");
}

