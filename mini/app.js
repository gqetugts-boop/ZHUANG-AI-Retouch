function makeDataUrl(value) {
  if (!value) return "";
  if (/^data:image\//.test(value) || /^https?:\/\//.test(value)) return value;
  return `data:image/png;base64,${value}`;
}

function collectImageUrls(value, results = []) {
  if (!value) return results;

  if (typeof value === "string") {
    const trimmed = value.trim();
    if (/^(https?:\/\/|data:image\/)/.test(trimmed)) results.push(trimmed);
    return results;
  }

  if (Array.isArray(value)) {
    value.forEach((item) => collectImageUrls(item, results));
    return results;
  }

  if (typeof value !== "object") return results;

  ["url", "image", "image_url", "output", "b64_json", "response_url", "download_url", "uri", "src"].forEach((key) => {
    if (typeof value[key] === "string") results.push(key === "b64_json" ? makeDataUrl(value[key]) : value[key]);
  });

  ["data", "results", "images", "result", "output", "outputs", "items", "attachments"].forEach((key) => collectImageUrls(value[key], results));
  return results;
}

function isLikelyImageUrl(url) {
  if (!url) return false;
  if (/^data:image\//i.test(url)) return true;

  try {
    const parsed = new URL(url.replaceAll("\\/", "/"));
    return /\.(png|jpe?g|webp|gif|avif)(?:$|[?#])/i.test(parsed.pathname) || /(?:^|\.)(oaidalleapiprodscus|blob|grs|claude|image|img|cdn)\./i.test(parsed.hostname);
  } catch (error) {
    return /\.(png|jpe?g|webp|gif|avif)(?:$|[?#])/i.test(url);
  }
}

function extractImagesFromResponse(responseData) {
  const images = collectImageUrls(responseData, []);
  const serialized = JSON.stringify(responseData || {});
  const urlMatches = serialized.match(/https?:\\?\/\\?\/[^"'\\\s]+/g) || [];
  const dataMatches = serialized.match(/data:image\/[a-zA-Z0-9.+-]+;base64,[A-Za-z0-9+/=]+/g) || [];
  return Array.from(new Set(images.concat(urlMatches.map((url) => url.replaceAll("\\/", "/")), dataMatches).filter(isLikelyImageUrl)));
}

async function fetchImageAsDataUrl(src) {
  if (!src || /^data:image\//i.test(src)) return src;
  if (!/^https?:\/\//i.test(src)) return src;

  try {
    const response = await fetch(src);
    if (!response.ok) return src;
    const blob = await response.blob();
    return await new Promise((resolve) => {
      const reader = new FileReader();
      reader.onloadend = () => resolve(String(reader.result || src));
      reader.onerror = () => resolve(src);
      reader.readAsDataURL(blob);
    });
  } catch (error) {
    return src;
  }
}

